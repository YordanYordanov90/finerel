export function searchTickers(query: string, limit = 8): TickerEntry[] {
  const q = query.toUpperCase().trim();
  if (!q) return [];

  const exactSymbol: TickerEntry[] = [];
  const symbolPrefix: TickerEntry[] = [];
  const nameMatch: TickerEntry[] = [];

  for (const entry of TICKERS) {
    if (entry.symbol === q) {
      exactSymbol.push(entry);
    } else if (entry.symbol.startsWith(q)) {
      symbolPrefix.push(entry);
    } else if (entry.name.toUpperCase().includes(q)) {
      nameMatch.push(entry);
    }
  }

  return [...exactSymbol, ...symbolPrefix, ...nameMatch].slice(0, limit);
}

export function isKnownTicker(symbol: string): boolean {
  const s = symbol.toUpperCase();
  return TICKERS.some((t) => t.symbol === s);
}
